## Operators 
print(10 * 6) ## Multiply
print(10 + 6) ## Add
print(10 - 6) ## Subtract
print(10 / 6) ## Divide

print(10 ** 6) ## Power of
print(10 % 6) ## Remainder

## Fuction abs (returns the absolute value)
print(abs(10.5))
print(abs(-10.5))

## Function pow, max and min
print(pow(2,5))
print(pow(6, 3))
print(max(15, 30, 58))
print(min(15, 30, 58))

## different forms
print("5*6") # text
print('5*6') # text
print(5*6)   # number

